#include "vector.h"

t_matrix4	m4_mul(t_matrix4 m1, double d)
{
	return ((t_matrix4){
		vmul4(m1.v1, d),
		vmul4(m1.v2, d),
		vmul4(m1.v3, d),
		vmul4(m1.v4, d),
	});
}
