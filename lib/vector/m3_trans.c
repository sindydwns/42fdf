#include "vector.h"

t_matrix3	m3_trans(t_matrix3 m1)
{
	return ((t_matrix3){
		(t_vector3){m1.v1.x, m1.v2.x, m1.v3.x},
		(t_vector3){m1.v1.y, m1.v2.y, m1.v3.y},
		(t_vector3){m1.v1.z, m1.v2.z, m1.v3.z}
	});
}
